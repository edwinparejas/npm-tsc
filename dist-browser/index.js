/**
 * Adds two numbers
 * @param a Number one
 * @param b Number two
 * @returns Result
 */
var add = function (a, b) { return a + b; };
/**
 * Substracts two numbers
 * @param a Number one
 * @param b Number two
 * @returns Result
 */
var substract = function (a, b) { return a - b; };
/**
 * Multiplies two numbers
 * @param a Number one
 * @param b Number two
 * @returns Result
 */
var multiply = function (a, b) { return a * b; };
export default {
    add: add,
    substract: substract,
    multiply: multiply
};
